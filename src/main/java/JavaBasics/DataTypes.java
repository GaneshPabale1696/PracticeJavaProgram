package JavaBasics;

public class DataTypes {

	// main() method--starting and execution point of the program
	public static void main(String[] args) {
		// primitive data types:int ,double,char,boolean

		// 1. int
		int i = 10;
		i = 20;
		int j = 30;
		int k = 40;
		int l = -1;
		int p = 0;

		System.out.println(i + j);

		// 2.float
		float f = 12.45f;

		// 3.double
		double y = 23.34;
		double t = 19;
		double t1 = 23.45;

		// 3.char:only single digit value,should be written in single qoutes
		char c = 'a';
		char c1 = 'A';
		char c2 = '1';
		char c3 = '$';

		// 4. boolean:true or false also called as keyword
		boolean b1 = true;
		boolean b2 = false;

		// 5.String: is a class,not a data type
		String s = "Hello world";
		String s1 = "selenium";
		String s2 = "Hi this is java code";
		String s3 = "1000";
		String s4 = "12.34";

		System.out.println("value of s2 is:" + s2);

		// System is a class
		System.out.println("value of j is:" + j);
	}

}
